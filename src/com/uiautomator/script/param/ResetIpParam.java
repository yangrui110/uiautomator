package com.uiautomator.script.param;

public class ResetIpParam {

    //ip地址
    private String ip;
    //端口号
    private Integer port;
    //手机端的端口号
    private Integer phonePort;

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public Integer getPort() {
        return port;
    }

    public void setPort(Integer port) {
        this.port = port;
    }

    public Integer getPhonePort() {
        return phonePort;
    }

    public void setPhonePort(Integer phonePort) {
        this.phonePort = phonePort;
    }
}
